# GitHub 上傳/update 流程: 
  
  # 打開 cmd
  # cd 你要上傳檔案所在的資料夾路徑
  # 如果你要上傳該資料夾路徑內的所有資料 >> 輸入 git add .
  # 如果你要上傳的是單一特定資料 >> 輸入 git add 檔案名稱
  # git commit -m "版本名稱 (隨便你想打什麼名稱)"   # commit 版本的意思 version 的含意
  # git push origin master >> 按下Enter 使其運行並等 cmd 上傳資料到 GitHub
 


